package OperatorsAndStatements;

public class ArthmeticOperators {

	public static void main(String[] args) {
		int y=4; 
		
		double x = 3+2 * --y;
		
		//decrement y to 3 first , multiply y with 2 , add it with 3
		System.out.println(x);
		System.out.println(y);
		
		int y1 = 10/2;  //quotient 5
	
		int y2 =10%2;  //remainder 0

		System.out.println(y1);
		System.out.println(y2);
	}
	

}
